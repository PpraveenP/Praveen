package Day6;

public class Pojo {
	
	String name;
	int id;
	String branch;
	
	public void setName(String name)
	{
		this.name=name;
		
	}
	public void setId(int id)
	{
		this.id=id;
	}
	public void setBranch(String branch)
	{
		this.branch=branch;
		
	}
	public String getName()
	{
		return name;
	}
	public int getID()
	{
		return id;
	}
	public String getBranch()
	{
		return branch;
	}
}
